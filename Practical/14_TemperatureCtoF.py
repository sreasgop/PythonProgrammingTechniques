# QUESTION:
# Write a program to convert a temperature in Celsius to a temperature in Fahrenheit. 



# CODE:
celcius = float(input("Enter temperature in Celsius: "))
f = (celcius * 1.8) + 32
print(f"Temperature in Fahrenheit: {f}")



# OUTPUT:
# Enter temperature in Celsius: 32.5
# Temperature in Fahrenheit: 90.5
