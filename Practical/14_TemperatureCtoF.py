# QUESTION:
# Write a program to convert a temperature in Celsius to a temperature in Fahrenheit. 



# CODE:
celsius = float(input("Enter temperature in Celsius: "))
f = (celsius * 1.8) + 32
print(f"Temperature in Fahrenheit: {f}")



# OUTPUT:
# Enter temperature in Celsius: 32.6
# Temperature in Fahrenheit: 90.68
